// Supported languages (no Chinese)
export const LANGUAGES = [
  { code: 'en', label: 'English', flag: 'EN' },
  { code: 'ja', label: '日本語', flag: 'JA' },
  { code: 'ko', label: '한국어', flag: 'KO' },
  { code: 'es', label: 'Espanol', flag: 'ES' },
  { code: 'fr', label: 'Francais', flag: 'FR' },
  { code: 'de', label: 'Deutsch', flag: 'DE' },
  { code: 'pt', label: 'Portugues', flag: 'PT' },
];

function detectLanguage() {
  const saved = localStorage.getItem('yt_lang');
  if (saved && translations[saved]) return saved;

  const nav = navigator.language?.toLowerCase() || 'en';
  // Check for Chinese — fallback to English
  if (nav.startsWith('zh')) return 'en';
  const match = LANGUAGES.find(l => nav.startsWith(l.code));
  return match ? match.code : 'en';
}

let currentLang = detectLanguage();
let listeners = [];

export function getLang() {
  return currentLang;
}

export function setLang(code) {
  if (translations[code]) {
    currentLang = code;
    localStorage.setItem('yt_lang', code);
    listeners.forEach(fn => fn(code));
  }
}

export function subscribe(fn) {
  listeners.push(fn);
  return () => { listeners = listeners.filter(f => f !== fn); };
}

export function t(key, params = {}) {
  let str = translations[currentLang]?.[key] || translations.en[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  });
  return str;
}

export function formatTime(totalMin, lang) {
  const l = lang || currentLang;
  const tr = translations[l] || translations.en;
  const h = Math.floor(totalMin / 60);
  const m = totalMin % 60;
  if (h > 0) {
    return tr.hFormat.replace('{h}', h).replace('{m}', m);
  }
  return tr.mFormat.replace('{m}', m);
}

// React hook — call useLang() in any component to re-render on language switch
import { useState, useEffect } from 'react';

export function useLang() {
  const [, tick] = useState(0);
  useEffect(() => subscribe(() => tick(n => n + 1)), []);
  return currentLang;
}
